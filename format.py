

def format_date(date):
	# year dictionary
	year_dict = {"2010" : "A" , "2011" : "B" , "2012" : "C" , "2013" : "D" , "2014" : "E", "2015" : "F" , "2016" : "H" ,
	"2017" : "J" , "2018" : "K" , "2019" : "L" , "2020" : "M" , "2021" : "N" , "2022" : "P" , "2023" : "R" , "2024" : "S" ,
	"2025" : "T" , "2026" : "U" , "2027" : "V" , "2028" : "W" , "2029" : "X"}

	# month dictionary
	month_dict = {"01" : "1" , "02" : "2" , "03" : "3" , "04" : "4" , "05" : "5" , "06" : "6" , "07" : "7" , "08" : "8" ,
	"09" : "9" , "10" : "O" , "11" : "N" , "12" : "D" }
	return f"{year_dict.get(date[:-2])}{month_dict.get(date[-2:])}"